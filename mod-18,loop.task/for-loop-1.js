/***

Subtask-1:

Find all the odd numbers from 61 to 100.

 */
for (let index = 61; index < 100; index++) {
  if (index % 2 !== 0) {
    console.log(index, "odd number");
  }
}

/***

Subtask-2:

Find all the even numbers from 78 to 98.

 */
for (let i = 78; i < 98; i++) {
  if (i % 2 === 0) {
    console.log(i, "even number");
  }
}

